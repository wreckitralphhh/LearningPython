# Turn the if-else chain from 5-4 into an if-elif-else
alien_color = 'red'

# If the alien is green, the player earned 5 points
if alien_color == 'green':
    print("You've earned 5 points!")
# If the alien is yellow, the player earned 10 points
elif alien_color == 'yellow':
    print("You've earned 10 points!")
# If the alien is red, the player earned 15 points
else:
    print("You've earned 15 points!")